package com.apnacollege;

public class CWH_06_Operators {
    public static void main(String[] args) {
        int a = 4;
        int b = 6%a ; //module operator //remainder = 2
        System.out.println(b);
        System.out.println(65<6);//comaprison operator
        //logical operator
        System.out.println(64>5  && 64>8);//and operator
        System.out.println(44>3 || 2<5);//or operator
        //bitwise operator
        System.out.println(2&3);

    }
}
